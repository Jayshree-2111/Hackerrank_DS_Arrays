int hourglassSum(vector<vector<int>> arr) {
    int result=INT_MIN; //initialising the result variable with -infinity 
    for(int i=0;i<4;i++) //loop for row which will iterate for 3 times for forming 1 hour glass and then incremented for forming next hour glass
        for(int j=0;j<4;j++) ////loop for column which will iterate for 3 times for forming 1 hour glass and then incremented for forming next hour glass
        {
           
            int sum=arr[i][j]+arr[i][j+1]+arr[i][j+2]+arr[i+1][j+1]+arr[i+2][j]+arr[i+2][j+1]+arr[i+2][j+2]; //sum of only those elements which are forming an hour glass 
            
            result= std::max(result,sum); //comparing result with sum variable, once the sum is greater than result value then, result variable will be updated.
                                          
        }
        return result; //max result value will be returned back

}

